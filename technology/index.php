               <h1>
                  Why lean a vehicle?
               </h1>

               <h2>
                  During a cornering sequence the forces acting to roll a vehicle out of a corner are balanced by the vehicles ability to shift/lower its center of gravity.&nbsp; This ability provides the most effective and efficient method to safely navigate through a cornering sequence. 
                  <br />
                  <br />
                  Any vehicle design conducive to leaning suspension technology will realize significant improvements in stability and performance utilizing the WST system.&nbsp; Our core design focus is operator control and safety.  
                  <br />
                  <br />
                  The Wesll Ski-Machine employs proprietary leaning technology and custom skis&#8211;with edges, not standard snowmobile skids with carbides.&nbsp; Skis are engineered to deflect and carve, skids are not.&nbsp; Directional stability is imparted to our machine through ski camber and toe adjustment which provides approximately 8 feet of total effective edge.&nbsp; Conversely, the present day modern snowmobile is directed by approximately 6-24 inches of total carbide, usually located in the center of their short skids. 
                  <br />
                  <br />
                  Ideally a snowmobile needs to maximize the surface area it presents to the snow in order to provide optimal traction and flotation to prevent it from getting stuck.  
                  <br />
                  <br />
                  <b>Problem:</b> Increasing track size makes a machine more difficult to control on the trail.
                  <br />
                  <b>Solution:</b> Stop snowmobiling, start skiing.
                  <br />
                  <br />
                  Leaning is the solution, not only is it safer, it&#39;s much more fun.&nbsp; Skiing provides the ability to quickly change direction whilst maintaining control and stability.&nbsp; Operating a machine that leans outboard while cornering seems kind of&#8230;not so smart.
                  <br />
                  <br />
                  It is now possible to ski your snowmobile&#8230;the additional capabilities provided by the Wesll active suspension system improves the operators&#39; critical decision/response time.&nbsp; The result is increased control and safety with a considerable reduction in operator fatigue. 
                  <br />
                  <br />
                  Our Ski&#8211;Machine uses custom skis, with 8 feet effective edge, coupled to an intelligent leaning suspension providing quality feedback and high sensitivity to operator input.&nbsp; Our rounded long track is optimized for flotation and hook&#8211;up.&nbsp; The result is a hybrid system providing increased flotation in deep snow with superior directional stability and control on trail.&nbsp; The WST Ski&#8211;Machine is a welcome paradigm shift in terms of performance, control and safety&#8230;
               </h2>
