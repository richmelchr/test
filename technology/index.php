               <h1>Product Description / Characteristics</h1>  
               
               <h2>
                  The product is the world's first and only long travel leaning suspension system.&nbsp; A WST equipped vehicle provides long-travel suspension capability through all designed lean angles.&nbsp; Fluid operator control results from match-critical machining of high-performance materials.&nbsp; The WST system is an active (smart) system.&nbsp; Components in the steering column measure an operator's natural steering inputs and mass-shift.&nbsp; These measurements are transmitted to an ECU (Electronic Control Unit) which then affects control of a small hydraulic drive system to lean-assist the vehicle thereby substantially improving stability and control.&nbsp; The system is intrinsically safe.&nbsp; Electromechanical lean-assist, even at failure, cannot override operator control, which is insured via system geometry.
                  <br />
                  <br /> 
                  For the first time, a vehicle will have all the benefits of an off-road style long travel suspension while maintaining the control needed for road racing.
